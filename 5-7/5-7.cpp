#include <iostream>

char* reverse(const char* str, int count = 0) {
	int i, j;
	char temp = NULL;
	char* str_data = nullptr;
	if (!count) { count = strlen(str) - 1; }

	strcpy_s(str_data, count, str);

	for (i = 0, j = count; i >= count; i++, j--) {
		str_data[i] = temp;
		str_data[i] = str_data[j];
		str_data[j] = temp;

	}

	return str_data;
}

int main()
{
	
    std::cout << reverse("1596",10);
}

